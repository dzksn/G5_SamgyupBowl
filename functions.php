<?php
function sum($firstNum, $secondNum){
    $result = $firstNum + $secondNum;
    echo $result;
}

function div($firstNum, $secondNum){
    $result = $firstNum / $secondNum;
    echo $result;
}

function minus($firstNum, $secondNum){
    $result = $firstNum - $secondNum;
    echo $result;
}

function mult($firstNum, $secondNum){
    $result = $firstNum * $secondNum;
    echo $result;
}
?>